# Legacy Percent (%) Formatting

name = "Farhan"
greeting = "Hello, %s!" % name
print(greeting)

# Explanation:
# In this code snippet, we are using the legacy percent (%) formatting to create a greeting message.
# The %s is a placeholder for a string, and the value of the variable 'name' is inserted in its place.
# The output of this code will be: "Hello, Farhan!"