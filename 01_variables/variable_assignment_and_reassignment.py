# Example of variable assignment and reassignment in Python

data = 10
data = 20
data = 30

print(data)

# Explanation:
# In this code snippet, we are assigning the value 10 to the variable 'data', then we reassign it to 20, and finally to 30.
# When we print 'data', it will output 30, which is the last value assigned to it.
# This demonstrates that variables can be reassigned to new values, and the previous values are overwritten.