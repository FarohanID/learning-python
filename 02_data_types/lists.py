# Lists are ordered, mutable, and can contain duplicate elements.
# Mutable means that you can change the contents of a list after it has been created.

my_list = [1, 10, "Farhan", 3.14, True]
print(my_list)
print("Its data type is", type(my_list))

# Explaining the list elements:
# 1: An integer
# 10: Another integer
# "Farhan": A string
# 3.14: A floating-point number
# True: A boolean value
# Lists can contain elements of different data types, as shown above.