# Tuples are similar to lists but they are immutable. 
# Meaning that once a tuple is created, its elements cannot be changed. 
# They are defined using parentheses () instead of square brackets [].

my_tuple = (1, 10, "Farhan", 3.14, True)
print(my_tuple)
print("Its data type is", type(my_tuple))

# Explaining the tuple elements:
# 1: An integer
# 10: Another integer
# "Farhan": A string
# 3.14: A floating-point number
# True: A boolean value
# Tuples can contain elements of different data types, as shown above.