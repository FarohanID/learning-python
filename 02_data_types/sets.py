# Sets are unordered collections of unique items. 
# They are defined using curly braces {} or the set() constructor. 
# Sets do not allow duplicate values and do not maintain any specific order.

my_set = {1, 10, "Farhan", 3.14, True}
print(my_set)
print("Its data type is", type(my_set))

# Explaining the set elements:
# 1: An integer
# 10: Another integer
# "Farhan": A string
# 3.14: A floating-point number
# True: A boolean value
# Note: The order of elements in a set may not be the same as the order in which they were added, 
# and duplicate values will be automatically removed.