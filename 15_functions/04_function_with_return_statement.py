# function with return adalah fungsi yang mengembalikan nilai setelah dieksekusi, 
# sehingga kita dapat menyimpan hasilnya dalam variabel atau menggunakannya dalam operasi lain.

def tambah(a, b):
    return a + b # return digunakan untuk mengembalikan nilai hasil penjumlahan dari a dan b.

hasil = tambah(5, 3) # Menyimpan hasil dari fungsi tambah ke dalam variabel hasil.
print(hasil) # Output: 8, mencetak nilai hasil yang telah disimpan dalam variabel hasil.